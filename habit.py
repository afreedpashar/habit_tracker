import requests

GENDER = 'MALE'
WEIGHT_KG = 65
HEIGHT_CM = 170
AGE = 25

APP_ID = '295f409e'
API_KEY = "10468863128a7ae925cc0e77de921252"

exercise_endpoint = "https://trackapi.nutritionix.com/v2/natural/exercise"

exercise_text = input("Tell me which exercises you did: ")

headers = {
    "x-app-id": APP_ID,
    "x-app-key": API_KEY,
}

